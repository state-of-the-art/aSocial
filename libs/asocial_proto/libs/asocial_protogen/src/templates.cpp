inline google::protobuf::Timestamp toTimestamp(const QDateTime& dt)
{
    google::protobuf::Timestamp ts;
    ts.setSeconds(dt.toSecsSinceEpoch());
    ts.setNanos(dt.time().msec() * 1000000);
    return ts;
}

inline QDateTime fromTimestamp(const google::protobuf::Timestamp& ts)
{
    return QDateTime::fromMSecsSinceEpoch(ts.seconds() * 1000 + ts.nanos() / 1000000);
}

inline qint64 timestampToMsEpoch(const google::protobuf::Timestamp& ts)
{
    return ts.seconds() * 1000 + ts.nanos() / 1000000;
}

// ISO 8601 format "yyyy-MM-ddTHH:mm:ss" in UTC for use in database keys.
// Enables lexicographic filtering by date/time.
inline QString timestampToIso(const google::protobuf::Timestamp& ts)
{
    return fromTimestamp(ts).toString(Qt::ISODate);
}

inline QString timestampToIsoKey(const google::protobuf::Timestamp& ts)
{
    return fromTimestamp(ts).toUTC().toString(QStringLiteral("yyyy-MM-ddTHH:mm:ss"));
}

inline QString newId()
{
    return QUuid::createUuid().toString(QUuid::WithoutBraces);
}

