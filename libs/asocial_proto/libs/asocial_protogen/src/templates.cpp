void ValidationResult::addError(const QString& f, const QString& m)
{
    valid = false;
    errors.append(ValidationError(f, m));
}

void ValidationResult::addFK(const QString& f, const QString& te, const QString& tf,
                             const QString& kp, const QString& v)
{
    foreignKeys.append(ForeignKeyRef(f, te, tf, kp, v));
}

QString ValidationResult::toString() const
{
    QStringList parts;
    for( const auto& e : errors )
        parts << e.field + QStringLiteral(": ") + e.message;
    return parts.join(QStringLiteral("; "));
}

AccessPermissions expandPermissions(AccessPermissions p)
{
    if( p.testFlag(AccessCategory::ProfileWrite) )
        p |= AccessCategory::ProfileRead;
    if( p.testFlag(AccessCategory::DataWrite) )
        p |= AccessCategory::DataRead;
    if( p.testFlag(AccessCategory::SettingsWrite) )
        p |= AccessCategory::SettingsRead;
    if( p.testFlag(AccessCategory::ContainerWrite) )
        p |= AccessCategory::ContainerRead;
    return p;
}

QStringList permissionNames(AccessPermissions p)
{
    QStringList n;
    if( p.testFlag(AccessCategory::ProfileRead) )    n << QStringLiteral("ProfileRead");
    if( p.testFlag(AccessCategory::ProfileWrite) )   n << QStringLiteral("ProfileWrite");
    if( p.testFlag(AccessCategory::DataRead) )       n << QStringLiteral("DataRead");
    if( p.testFlag(AccessCategory::DataWrite) )      n << QStringLiteral("DataWrite");
    if( p.testFlag(AccessCategory::SettingsRead) )   n << QStringLiteral("SettingsRead");
    if( p.testFlag(AccessCategory::SettingsWrite) )  n << QStringLiteral("SettingsWrite");
    if( p.testFlag(AccessCategory::ContainerRead) )    n << QStringLiteral("ContainerRead");
    if( p.testFlag(AccessCategory::ContainerWrite) )  n << QStringLiteral("ContainerWrite");
    if( p.testFlag(AccessCategory::AppLifecycle) )   n << QStringLiteral("AppLifecycle");
    if( p.testFlag(AccessCategory::ExportImport) )   n << QStringLiteral("ExportImport");
    if( p.testFlag(AccessCategory::PluginAccess) )   n << QStringLiteral("PluginAccess");
    if( n.isEmpty() ) n << QStringLiteral("None");
    return n;
}

