class ForeignKeyRef {
public:
    QString field;
    QString targetEntity;
    QString targetField;
    QString lookupKeyPrefix;
    QString value;

    ForeignKeyRef() = default;
    ForeignKeyRef(QString field, QString targetEntity, QString targetField,
                  QString lookupKeyPrefix, QString value)
        : field(std::move(field)), targetEntity(std::move(targetEntity)),
          targetField(std::move(targetField)), lookupKeyPrefix(std::move(lookupKeyPrefix)),
          value(std::move(value))
    {
    }
};

class ValidationResult {
public:
    bool valid = true;
    QList<ValidationError> errors;
    QList<ForeignKeyRef> foreignKeys;

    void addError(const QString& f, const QString& m);
    void addFK(const QString& f, const QString& te, const QString& tf,
               const QString& kp, const QString& v);
    QString toString() const;
};

using KeyExistsFn = std::function<bool(const QString&)>;

// ============================================================
// Access Permission System
// ============================================================

